        public static string GetByExtension(string extension)
            => extension.ToUpper() switch
            {
                "PDF" => PDF,
                "DOC" => DOC,
                "DOCX" => DOCX,
                "XLS" => XLS,
                "XLSX" => XLSX,
                "PNG" => PNG,
                "JPEG" or "JPG" => JPEG,
                "GIF" => GIF,
                "CSV" => CSV,
                _ => throw new System.Exception("Extension not defined!!!")
            };
